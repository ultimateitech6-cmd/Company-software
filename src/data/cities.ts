export function getCityStaticParams() {
  return targetCities.map(({ slug }) => ({ city: slug }));
}

export function getCityName(city: string) {
  const targetCity = targetCities.find(({ slug }) => slug === city);

  if (targetCity) {
    return targetCity.name;
  }

  return city
    .split("-")
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(" ");
}
